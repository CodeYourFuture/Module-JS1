// implement a function countChar that counts the number of times a character occurs in a string

// Given a string str and a single character char to search for,
// When the countChar function is called with these inputs,
// Then it should:

function countChar(str, char) {
  let count = 0;
  for (let i = 0; i < str.length; i++) {
    if (str[i] === char) {
      count++;
    }
  }
  return count;
}

test("checks the occurrences of a char in a string", function () {
  expect(countChar("my name is marcus", "m")).toBe(3);
});

test("checks the occurrences of a char in a string", function () {
  expect(countChar("hello there world", "o")).toBe(2);
});

test("checks the str input is actually a string", function () {
  expect(countChar("How are you?", "b")).toBe(0);
});


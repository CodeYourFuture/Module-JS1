let firstName = "Creola";
let middleName = "Katherine";
let lastName = "Johnson";

// Declare a variable called initials that stores the first character of each string in upper case to form the user's initials
// Log the variable in each case

initialFirstName = firstName.charAt(0);
initialMiddleName = middleName.charAt(0);
initialLastName = lastName.charAt(0);

initials =
  initialFirstName.toUpperCase() +
  initialMiddleName.toUpperCase() +
  initialLastName.toUpperCase();

console.log(initials);
